#include "game.h"
#include <stdlib.h>

game** generateGames() {
    int puzzles[10][3][3] = {
        {{1, 6, 5}, {4, 0, 3}, {7, 8, 2}},
        {{7, 6, 0}, {8, 3, 1}, {2, 4, 5}},
        {{7, 1, 8}, {3, 6, 2}, {5, 4, 0}},
        {{3, 4, 7}, {5, 0, 2}, {8, 6, 1}},
        {{4, 5, 3}, {7, 0, 1}, {8, 6, 2}},
        {{8, 5, 3}, {7, 0, 4}, {2, 1, 6}},
        {{0, 1, 3}, {8, 2, 5}, {6, 4, 7}},
        {{1, 3, 0}, {7, 8, 6}, {5, 2, 4}},
        {{1, 7, 5}, {6, 2, 3}, {8, 4, 0}},
        {{0, 6, 3}, {2, 1, 7}, {5, 8, 4}}
    };

    game** games = malloc(10 * sizeof(game*));

    for (int i = 0; i < 10; i++) {
        games[i] = newGame();
        for (int x = 0; x < 3; x++)
            for (int y = 0; y < 3; y++)
                games[i]->grid[x][y] = puzzles[i][x][y];
    }

    return games;
}